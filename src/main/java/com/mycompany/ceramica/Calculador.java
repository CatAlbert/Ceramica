    public static boolean esArcillaRoja(double siO2, double al2o3, double fe2o3, double cao, double mgo, double na2o, double k2o, double tio2, double ppc) {
        return (fe2o3 > 3.5 && fe2o3 < 8) &&
               (siO2 > 30 && siO2 < 65) &&
               (al2o3 > 12 && al2o3 < 25) &&
               (mgo > 0.8 && mgo < 3) &&
               (na2o > 0.1 && na2o < 0.9) &&
               (k2o > 3 && k2o < 5) &&
               (tio2 < 1.2) &&
               ((cao > 0.5 && cao < 2) || (cao > 2 && cao < 5) || (cao > 10 && cao < 25)) &&
               ((ppc > 15 && ppc < 23) || (ppc > 7 && ppc < 10) || (ppc < 6));
    }

    public static boolean esArcillaBlanca(double siO2, double al2o3, double fe2o3, double cao, double mgo, double na2o, double k2o, double tio2, double ppc) {
        return (fe2o3 > 0.8 && fe2o3 < 5) &&
               (siO2 > 48 && siO2 < 79) &&
               (al2o3 > 15 && al2o3 < 35) &&
               (mgo < 0.8) &&
               (na2o > 0.1 && na2o < 1) &&
               (k2o > 0.7 && k2o < 3.5) &&
               (tio2 > 0.9 && tio2 < 2) &&
               (cao < 0.9) &&
               (ppc > 3.5 && ppc < 12);
    }

    public static boolean esCaolin(double siO2, double al2o3, double fe2o3, double cao, double mgo, double na2o, double k2o, double tio2, double ppc) {
        return (fe2o3 > 0.3 && fe2o3 < 2) &&
               (siO2 > 45 && siO2 < 60) &&
               (al2o3 > 25 && al2o3 < 38) &&
               (mgo < 0.4) &&
               (na2o > 0.04 && na2o < 0.8) &&
               (k2o > 0.5 && k2o < 2.5) &&
               (tio2 < 0.5) &&
               (cao > 0.02 && cao < 0.22) &&
               (ppc > 8 && ppc < 13.6);
    }

    public static boolean esArenaFeldespatica(double siO2, double al2o3, double fe2o3, double cao, double mgo, double na2o, double k2o, double tio2, double ppc) {
        return (fe2o3 > 0.05 && fe2o3 < 0.15) &&
               (siO2 > 80 && siO2 < 97) &&
               (al2o3 > 2 && al2o3 < 11) &&
               (mgo < 0.2) &&
               (na2o > 0.1 && na2o < 0.6) &&
               (k2o > 1 && k2o < 4) &&
               (tio2 < 0.03) &&
               (cao < 0.08) &&
               (ppc < 2);
    }

    public static boolean esCarbonato(double siO2, double al2o3, double fe2o3, double cao, double mgo, double ppc) {
        return ((cao > 50 && cao < 56) && (mgo < 2.5) && (ppc > 40 && ppc < 45) && (siO2 < 3) && (al2o3 < 1)) ||
               ((cao > 30) && (mgo > 20) && (ppc > 45) && (siO2 < 0.5) && (al2o3 < 1)) ||
               ((cao > 0.1 && cao < 3) && (mgo > 90) && (ppc < 1) && (siO2 < 3) && (al2o3 < 1));
    }

    public static boolean esTalco(double siO2, double al2o3, double fe2o3, double cao, double mgo, double ppc) {
        return (fe2o3 > 0.6 && fe2o3 < 2) &&
               (siO2 > 45 && siO2 < 62) &&
               (al2o3 > 0.5 && al2o3 < 10) &&
               (mgo > 25 && mgo < 35) &&
               (cao > 0.5 && cao < 2) &&
               (ppc > 7 && ppc < 9);
    }

