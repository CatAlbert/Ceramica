    public void CheckMarronEste(double siO2, double al2o3, double fe2o3, double cao, double mgo, double na2o, double k2o, double tio2, double ppc) {
        if (esArcillaRoja(siO2, al2o3, fe2o3, cao, mgo, na2o, k2o, tio2, ppc)) {
            jLabel10.setText("Es arcilla Roja");
        } else if (esArcillaBlanca(siO2, al2o3, fe2o3, cao, mgo, na2o, k2o, tio2, ppc)) {
            jLabel10.setText("Es arcilla Blanca");
        } else if (esCaolin(siO2, al2o3, fe2o3, cao, mgo, na2o, k2o, tio2, ppc)) {
            jLabel10.setText("Es Caolin");
        } else if (esArenaFeldespatica(siO2, al2o3, fe2o3, cao, mgo, na2o, k2o, tio2, ppc)) {
            jLabel10.setText("Es Arena Feldespatica");
        } else if (esCarbonato( siO2,  al2o3,  fe2o3,  cao,  mgo,  ppc)) {
            jLabel10.setText("Es Carbonato");
        } else if (esTalco( siO2,  al2o3,  fe2o3,  cao,  mgo,  ppc)) {
            jLabel10.setText("Es Talco");
        } else {
            jLabel10.setText("No es un valor valido");
        }
    }
